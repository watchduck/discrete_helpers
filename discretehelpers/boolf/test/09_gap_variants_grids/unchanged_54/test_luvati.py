from discretehelpers.boolf.examples.e09_gap_variants_grids import luvati, luvati_fs


def test_luvati():
    assert luvati.fullspots == luvati_fs
    assert luvati.splits == [
        ({3, 11, 123, 59, 27}, {0, 2, 4, 8, 120, 60, 124, 24, 122, 28}),  # a 0 0
        ({2, 3, 11, 123, 122, 59, 27}, {0, 4, 8, 120, 60, 124, 24, 28}),  # b 1 1
        ({124, 4, 28, 60}, {0, 2, 3, 8, 59, 11, 123, 24, 122, 27, 120}),  # d 3 2
        ({8, 59, 11, 123, 120, 60, 124, 24, 122, 27, 28}, {0, 2, 3, 4}),  # e 4 3
        ({27, 123, 120, 60, 124, 24, 122, 59, 28}, {0, 2, 3, 4, 8, 11}),  # f 5 4
        ({120, 123, 122, 59, 124, 60}, {0, 2, 3, 4, 8, 11, 24, 27, 28}),  # g 6 5
        ({120, 122, 123, 124}, {0, 2, 3, 4, 8, 59, 11, 60, 24, 27, 28})   # h 7 6
    ]
