from discretehelpers.boolf.examples.e09_gap_variants_grids import takeli, takeli_fs


def test_takeli():
    assert takeli.fullspots == takeli_fs
    assert takeli.splits == [
        ({3, 11, 123, 59, 27}, {0, 2, 4, 58, 122, 10, 12, 60, 124, 26, 28}),  # a 0 0
        ({2, 3, 58, 122, 59, 10, 11, 123, 26, 27}, {0, 124, 4, 12, 28, 60}),  # b 1 1
        ({124, 4, 12, 28, 60}, {0, 2, 3, 58, 122, 59, 10, 11, 123, 26, 27}),  # d 3 2
        ({58, 122, 59, 10, 11, 12, 123, 60, 124, 26, 27, 28}, {0, 2, 3, 4}),  # e 4 3
        ({58, 122, 27, 123, 60, 124, 26, 59, 28}, {0, 2, 3, 4, 10, 11, 12}),  # f 5 4
        ({58, 123, 122, 59, 124, 60}, {0, 2, 3, 4, 10, 11, 12, 26, 27, 28}),  # g 6 5
        ({122, 123, 124}, {0, 2, 3, 4, 58, 59, 10, 11, 12, 60, 26, 27, 28})   # h 7 6
    ]