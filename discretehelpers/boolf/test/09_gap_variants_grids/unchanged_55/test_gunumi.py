from discretehelpers.boolf.examples.e09_gap_variants_grids import gunumi, gunumi_fs


def test_gunumi():
    assert gunumi.fullspots == gunumi_fs
    assert gunumi.splits == [
        ({51, 3, 243}, {0, 12, 48, 240, 18, 114, 20, 116, 252, 60}),  # a 0
        ({18, 3, 51, 114, 243}, {0, 12, 48, 240, 20, 116, 252, 60}),  # b 1
        ({116, 20, 252, 12, 60}, {0, 3, 48, 240, 18, 51, 114, 243}),  # c 2
        ({12, 252, 60}, {0, 3, 48, 240, 18, 51, 20, 114, 116, 243}),  # d 3
        ({48, 240, 18, 51, 20, 114, 116, 243, 252, 60}, {0, 3, 12}),  # e 4
        ({48, 240, 114, 51, 116, 243, 252, 60}, {0, 18, 3, 20, 12}),  # f 5
        ({240, 114, 243, 116, 252}, {0, 3, 12, 48, 18, 51, 20, 60}),  # g 6
        ({240, 243, 252}, {0, 3, 12, 48, 18, 51, 20, 114, 116, 60})   # h 7
    ]
