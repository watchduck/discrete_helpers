arity_to_dict_of_quadrants = {1: dict_1, 2: dict_2, 3: dict_3}


def test():

    for arity in [1, 2, 3]:
        dict_of_quadrants = arity_to_dict_of_quadrants[arity]
        for intval, quadrants in dict_of_quadrants.items():
            patron_quadrant, weight_quadrant = quadrants
            a = Boolf(intval, arity=arity)
            b = a.complement
            assert a.patron_quadrant(arity) == patron_quadrant
            assert a.weight_quadrant(arity) == weight_quadrant
            assert b.patron_quadrant(arity) == 3 - patron_quadrant
            assert b.weight_quadrant(arity) == 3 - weight_quadrant
